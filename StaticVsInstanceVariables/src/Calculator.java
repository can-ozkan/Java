public class Calculator {

    public static void printSum(int num1, int num2){
        System.out.println("Sum is " + (num1 + num2));
    }

    public static int sum(int num1, int num2){
        return num1 + num2;
    }

    public static double sum(double num1, double num2){
        return num1 + num2;
    }
}
